export type ContentMap = Record<string, string>;

// Чете всички въведени стойности и ги слива върху подразбиращите се.
export async function getContent(): Promise<ContentMap> {
  let stored: Record<string, string> = {};
  try {
    const { data } = await db().from("site_content").select("key, value");
    for (const r of data ?? []) {
      if (r.value != null && String(r.value).trim() !== "") stored[r.key] = r.value;
    }
  } catch {
    stored = {};
  }
  return { ...CONTENT_DEFAULTS, ...stored };
}

// Списък: по 1 елемент на ред
export function clines(value: string): string[] {
  return (value ?? "")
    .split("\n")
    .map((s) => s.trim())
    .filter(Boolean);
}

// Двойки „ляво | дясно" на ред
export function cpairs(value: string): { a: string; b: string }[] {
  return clines(value).map((line) => {
    const i = line.indexOf("|");
    return i < 0
      ? { a: line.trim(), b: "" }
      : { a: line.slice(0, i).trim(), b: line.slice(i + 1).trim() };
  });
}
